package firstclass;

class Company
{
	public static void santro()
	{
		System.out.println("By hyundai");
	}
	public static void dezire()
	{
		System.out.println("By Maruti");
	}
	
}

public class UseOfStatic {

	public static void main(String[] args) {
		
		Company.santro();
		Company.dezire();

	}

}

